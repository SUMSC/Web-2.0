
<h2>开发说明</h2>
<pre>
1. header.php 和 footer.php 从Index文件夹中加载(参考IndexView.class.php 的代码)。

2. 每个人自己页面的元素，请在msc.css 文件中添加样式，并且一个人的代码写在一块内
   代码段前参照已有注释格式编写注释。

3. 不要修改项目中除application, css, js, images, fonts, sql 文件夹以外的程序。

4. 数据库统一使用msc作为用户，MSC作为数据库名。只需将msc.sql文件导入数据库即可。
   关于数据库用户的问题，翻看SDOJ群的聊天记录。

5. 前端代码请参照Index/文件夹内的代码，不要在header和footer文件中添加块元素。

更多其他问题，请谨慎操作。
</pre>

